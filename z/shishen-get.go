//传入四柱
func (r *ShiShenGet) Get(z *SiZhuModel) *ShiShenModel {
	if z == nil {
		return nil
	}
	riGan := z.RiGan
	bean := &ShiShenModel{
		NianGan: r.getSingle(riGan, z.NianGan),
		YunGan:  r.getSingle(riGan, z.YueGan),
		RiGan:   "日",
		ShiGan:  r.getSingle(riGan, z.ShiGan),
	}
	return bean
}

func (r *ShiShenGet) getSingle(riGan, gan2 string) string {
	str := riGan + gan2
	s, ok := SHI_SHEN_GAN[str]
	if ok {
		return s
	}
	return ""
}
